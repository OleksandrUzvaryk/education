package lesson08arrays_d_22_11.tasks.onemore;

public class ArrayResult {

    private int maxElement;
    private int indexMaxElement;

    public ArrayResult(int maxElement, int indexMaxElement) {
        this.maxElement = maxElement;
        this.indexMaxElement = indexMaxElement;
    }

    public int getMaxElement() {
        return maxElement;
    }

    public void setMaxElement(int maxElement) {
        this.maxElement = maxElement;
    }

    public int getIndexMaxElement() {
        return indexMaxElement;
    }

    public void setIndexMaxElement(int indexMaxElement) {
        this.indexMaxElement = indexMaxElement;
    }
}
