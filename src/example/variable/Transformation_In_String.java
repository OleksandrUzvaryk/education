package example.variable;

public class Transformation_In_String {

    public static void main(String[] args) {

        int number = 345;
        String numberString = String.valueOf(number);
        System.out.println(numberString);

        System.out.println(numberString.charAt(0));
        System.out.println(numberString.charAt(1));
        System.out.println(numberString.charAt(2));

    }

}
